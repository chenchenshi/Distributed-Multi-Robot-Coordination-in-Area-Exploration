import numpy as np


def distance(cur_node,end_node):
    return abs(cur_node.x-end_node.x)+abs(cur_node.y-end_node.y)


class Node(object):
    def __init__(self,father,x,y,end):
        self.father=father
        self.x=x
        self.y=y
        self.end=end

        if father!=None:
            self.G=father.G+1
            self.H=distance(self,self.end)
            self.F=self.G+self.H
        else:
            self.G=0
            self.H=0
            self.F=0

    def set_father(self,father,new_G):
        if father!=Node:
            self.G=new_G
            self.F=self.G+self.F

        self.father=father


class Navigation(object):
    def __init__(self,map,start_x,start_y,end_x,end_y):
        self.maze=map
        self.end = Node(None, end_x, end_y,None)
        self.start=Node(None,start_x,start_y,self.end)
        self.open_list={}
        self.close_list={}
        self.map_border=map.shape
        self.orientation=[]
        for y,line in enumerate(map):
            for x,block in enumerate(line):
                if map[y][x]==1:
                    self.close_list[(x,y)]=Node(None,x,y,self.end)

    def min_F_node(self):
        if len(self.open_list)==0:
            raise Exception('不存在最小路径')

        min_=9999999999999999
        k_=(self.start.x,self.start.y)

        for k,v in self.open_list.items():
            if min_>v.F:
                k_=k

        return self.open_list[k_]


    def addAdjacentIntoOpen(self,node):
        # 先将列表从open_list添加到close_list中
        self.open_list.pop((node.x,node.y))
        self.close_list[(node.x,node.y)]=node
        adjacent=[]

        # 处理边界
        # 上
        try:
            adjacent.append(Node(node,node.x,node.y-1,self.end))
        except Exception as err:
            pass

        # 下
        try:
            adjacent.append(Node(node,node.x,node.y+1,self.end))
        except Exception as err:
            pass

        # 左
        try:
            adjacent.append(Node(node,node.x-1,node.y,self.end))
        except Exception as err:
            pass

        # 右
        try:
            adjacent.append(Node(node,node.x+1,node.y,self.end))
        except Exception as err:
            pass

        for a in adjacent:
            # 终点
            if (a.x,a.y)==(self.end.x,self.end.y):
                new_G=node.G+1
                self.end.set_father(node,new_G)
                return True

            # 如果在close_list中，则不用重复处理了
            if (a.x,a.y) in self.close_list:
                continue

            # 如果不在close_list中，则添加进去
            if (a.x,a.y) not in self.open_list:
                self.open_list[(a.x,a.y)]=a
            # 如果在open_list中而不在close_list中，则通过G值判断是否更接近
            else:
                exist_node=self.open_list[(a.x,a.y)]
                new_G=node.G+1
                if new_G<exist_node.G:
                    exist_node.set_father(node,new_G)

        return False

    def find_the_path(self):
        self.open_list[(self.start.x,self.start.y)]=self.start
        the_node=self.start
        try:
            while not self.addAdjacentIntoOpen(the_node):
                the_node=self.min_F_node()
        except Exception as err:
            print(err)
            return False

        return True

    def mark_path(self,node):
        if node.father==None:
            return

        # 左
        if node.father.x-node.x>0:
            self.orientation.append(1)
        # 右
        elif node.father.x-node.x<0:
            self.orientation.append(0)
        # 下
        elif node.father.y-node.y>0:
            self.orientation.append(2)
        # 上
        elif node.father.y-node.y<0:
            self.orientation.append(3)
        else:
            raise Exception('错误方向')

        self.mark_path(node.father)

    def navigate(self):
        if (self.start.x,self.start.y) in self.close_list or (self.end.x,self.end.y) in self.open_list:
            raise Exception('坐标不可走')

        if self.find_the_path():
            self.mark_path(self.end)

        self.orientation.reverse()
        str_ori=''
        for o in self.orientation:
            str_ori=str_ori+str(o)

        print('cmds:'+str_ori)

        return self.orientation


